    def save_characters(self):
        """保存角色档案"""
        if not self.characters_content:
            return False
        
        characters_file = self.project_dir / "角色档案.md"
        
        try:
            with open(characters_file, 'w', encoding='utf-8') as f:
                f.write(self.characters_content)
            
            # 更新工作流状态
            self._update_workflow_status()
            
            print(f"✅ 角色档案已保存：{characters_file}")
            return True
        except Exception as e:
            print(f"❌ 保存失败：{e}")
            return False
    
    def _update_workflow_status(self):
        """更新工作流状态"""
        config_file = self.project_dir / "config.json"
        if not config_file.exists():
            return
        
        try:
            with open(config_file, 'r', encoding='utf-8') as f:
                config = json.load(f)
            
            config['workflow']['step3_completed'] = True
            
            with open(config_file, 'w', encoding='utf-8') as f:
                json.dump(config, f, ensure_ascii=False, indent=2)
            
            print("✅ 工作流状态已更新：第3步完成")
        except:
            pass
    
    def run(self):
        """运行角色开发"""
        print("="*60)
        print("👤 第3步：角色开发")
        print("="*60)
        
        if not self.load_config():
            return False
        
        if self.generate_characters():
            if self.save_characters():
                print("\n" + "="*60)
                print("✅ 角色开发完成！")
                print("="*60)
                print("\n📋 下一步建议：")
                print("  short-drama-pro catalog    # 第4步：生成目录")
                print("\n💡 角色档案包含：")
                print("  • 主要角色详细设定")
                print("  • 次要角色功能说明")
                print("  • 人物关系图和冲突设置")
